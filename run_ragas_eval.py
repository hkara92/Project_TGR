"""
run_ragas_eval.py

Evaluates the predictions generated by run_eval.py using the RAGAS framework.
(Retrieval Augmented Generation Assessment)

Usage:
    python run_ragas_eval.py

Requirements:
    pip install ragas langchain-openai openpyxl pandas
"""

import os
import json
import logging
import pandas as pd
from dotenv import load_dotenv

# RAGAS Imports
from ragas import evaluate
from ragas.metrics import (
    faithfulness,
    answer_relevancy,
    context_recall,
    context_precision
)
from datasets import Dataset 
from langchain_openai import ChatOpenAI, OpenAIEmbeddings
from ragas.run_config import RunConfig  
import nest_asyncio

# --- CONFIGURATION ---
DATASET_NAME = "InfiniteChoice"
CACHE_DIR = "./cache"
BOOK_ID = "0"  # Example book ID to evaluate. Ideally, loop over all.

# Setup
load_dotenv()
nest_asyncio.apply()
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def evaluate_book_ragas(book_id):
    """Run RAGAS evaluation for a specific book's predictions."""
    
    # 1. Load predictions.json
    pred_path = os.path.join(CACHE_DIR, DATASET_NAME, book_id, "predictions.json")
    if not os.path.exists(pred_path):
        logger.error(f"Prediction file not found: {pred_path}")
        return

    logger.info(f"Loading predictions from {pred_path}...")
    with open(pred_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    if not data:
        logger.warning("Empty predictions file.")
        return

    # 2. Load nodes.json (the Tree/Chunks) to look up context text
    nodes_path = os.path.join(CACHE_DIR, DATASET_NAME, book_id, "nodes.json")
    if not os.path.exists(nodes_path):
        logger.error(f"Nodes file not found: {nodes_path}")
        return
    
    logger.info(f"Loading nodes from {nodes_path}...")
    try:
        with open(nodes_path, "r", encoding="utf-8") as f:
            nodes_map = json.load(f)
    except Exception as e:
        logger.error(f"Failed to load nodes.json: {e}")
        return

    # 3. Prepare Data for RAGAS
    # RAGAS expects: ['question', 'answer', 'contexts', 'ground_truth']
    
    questions = []
    answers = []
    contexts = []
    ground_truths = []
    
    for item in data:
        questions.append(item["question"])
        
        # Answer: Using raw_llm_output is better for "Answer Relevancy" if strict output is just "A".
        # If strict output is "A", Relevancy might be low. 
        # But faithfulness might fail if "A" is not a claim. 
        # We use raw_llm_output as it likely contains some text.
        ans_text = str(item.get("raw_llm_output", ""))
        answers.append(ans_text)
        
        # Contexts: Look up text using chunk_ids from retrieval_info
        ctx_list = []
        ret_info = item.get("retrieval_info", {})
        chunk_ids_dict = ret_info.get("chunk_ids", {})
        
        # Flatten and Deduplicate chunk IDs
        flat_ids = set()
        for c_list in chunk_ids_dict.values():
            flat_ids.update(c_list)
            
        for cid in flat_ids:
            # Handle standard chunk_X and L0_chunk_X conventions
            text = None
            if cid in nodes_map:
                text = nodes_map[cid]["text"]
            elif f"L0_{cid}" in nodes_map:
                text = nodes_map[f"L0_{cid}"]["text"]
            
            if text:
                ctx_list.append(text)
        
        contexts.append(ctx_list)
        
        # Ground Truth: The text of the correct option
        gt = item.get("ground_truth_text", item.get("ground_truth", ""))
        ground_truths.append(gt)

    # Validate lists
    if not questions:
        logger.warning("No valid data found.")
        return

    # Create HF Dataset
    ragas_dataset = Dataset.from_dict({
        "question": questions,
        "answer": answers,
        "contexts": contexts,
        "ground_truth": ground_truths
    })

    # 4. Setup RAGAS LLM (Using standard OpenAI, reusing project keys)
    llm = ChatOpenAI(model="gpt-4o", temperature=0)
    embeddings = OpenAIEmbeddings(model="text-embedding-3-large")
    
    # 5. Run Evaluation
    logger.info("Running RAGAS evaluation...")
    
    metrics = [
        faithfulness,
        answer_relevancy,
        context_recall,
        context_precision
    ]
    
    results = evaluate(
        ragas_dataset,
        metrics=metrics,
        llm=llm,
        embeddings=embeddings,
        raise_exceptions=False
    )
    
    # 6. Save Results
    output_dir = os.path.join(CACHE_DIR, DATASET_NAME, book_id)
    out_excel = os.path.join(output_dir, "ragas_results.xlsx")
    out_txt = os.path.join(output_dir, "ragas_summary.txt")
    
    df = results.to_pandas()
    df.to_excel(out_excel, index=False)
    
    logger.info(f"Saved RAGAS details to {out_excel}")
    
    # Summary
    summary = f"""
    RAGAS Results for Book {book_id}
    ================================
    Faithfulness:      {df['faithfulness'].mean():.4f}
    Answer Relevancy:  {df['answer_relevancy'].mean():.4f}
    Context Recall:    {df['context_recall'].mean():.4f}
    Context Precision: {df['context_precision'].mean():.4f}
    """
    print(summary)
    
    with open(out_txt, "w") as f:
        f.write(summary)

if __name__ == "__main__":
    # Example: run for book 0
    evaluate_book_ragas(BOOK_ID)
